import fs from 'fs';

const pngHeader = Buffer.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG Header
  0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52, // IHDR chunk start
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // Width 1, Height 1
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xde, // 8-bit RGB, etc.
  0x00, 0x00, 0x00, 0x0c, 0x49, 0x44, 0x41, 0x54, // IDAT chunk start
  0x08, 0xd7, 0x63, 0xf8, 0xff, 0xff, 0x3f, 0x00, 0x05, 0xfe, 0x02, 0xfe, 0xdc, 0x44, 0x74, 0x06, // Pixel data
  0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44, 0xae, 0x42, 0x60, 0x82 // IEND chunk
]);

const files = [
  'modern_furniture_banner.png',
  'bamboo_cane_lounge_chair.jpg',
  'bamboo_hanging_swing.jpg',
  'bamboo_coffee_table.jpg',
  'bamboo_modular_sofa.jpg',
  'bamboo_bed_frame.jpg',
  'blueprint_lounge_chair.jpg',
  'blueprint_swing_chair.jpg',
  'blueprint_coffee_table.jpg',
  'blueprint_modular_sofa.jpg',
  'blueprint_bed_frame.jpg'
];

const targetDir = './app/src/main/res/drawable/';

if (!fs.existsSync(targetDir)) {
  fs.mkdirSync(targetDir, { recursive: true });
}

files.forEach(file => {
  fs.writeFileSync(targetDir + file, pngHeader);
  console.log(`Created ${file}`);
});
